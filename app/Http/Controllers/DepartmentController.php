<?php

namespace App\Http\Controllers;

use App\Department;
use App\Mail\SystemError;
use Esl\helpers\Constants;
use Esl\Repository\NotificationRepo;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Mail;

class DepartmentController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return view('departments.index')
            ->withDepartments(Department::simplePaginate(25));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('departments.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Department::create($request->all());

        return redirect('/departments');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Department  $department
     * @return \Illuminate\Http\Response
     */
    public function show(Department $department)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Department  $department
     * @return \Illuminate\Http\Response
     */
    public function edit(Department $department)
    {
        return view('departments.edit')
            ->withDepartment($department);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Department  $department
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Department $department)
    {
        $department->update($request->all());
        return redirect('/departments');

    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Department  $department
     * @return \Illuminate\Http\Response
     */
    public function destroy(Department $department)
    {
        $department->delete();
        return redirect('/departments');
    }

    public function error(Request $request)
    {
        Mail::to(['email'=>'evans.ngala@esl-eastafrica.com'])
        ->cc(Constants::EMAILS_CC)
        ->send(new SystemError(['subject'=>'System Error',
            'body'=>$request->error]));

        NotificationRepo::create()->success('Error Reported Successfully');

        return redirect('/');
    }
}
